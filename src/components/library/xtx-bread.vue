<script>
import {h} from 'vue'
export default {
  name: "XtxBread",
 render(){
   const items = this.$slots.default()
   const dymanicItems = []
   items.forEach((item,i)=>{
     dymanicItems.push(item)
     if(i<(items.length-1)){
       dymanicItems.push(h('i',{class:'iconfont icon-arrow-right'}))
     }
   })
   return h('div',{class:'xtx-bread'},dymanicItems)
 }
};
</script>

